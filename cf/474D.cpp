void solve() {
    int t, k; re(t, k);
    V<V<Mint>> dp(N, V<Mint>(2, 0));
    dp[1][R] = 1;
    if (k == 1) {
        dp[1][W] = 1;
    }
    for (int i = 2; i < N; ++i) {
        // there are no more restrictions on red:
        // if the ith is red, then i-1 can be either red or white
        dp[i][R] = dp[i-1][R] + dp[i-1][W];
        if (i - k >= 0) {
            // but if the ith is white,
            // then the previous k should all be white
            // so we need to consider the options before the k elements
            dp[i][W] = dp[i-k][R];
            if (i - k == 0)
                dp[i][W] += 1;
            else
                dp[i][W] += dp[i-k][W];
        }
    }
    V<Mint> pf(N, 0);
    for (int i = 0; i < N; ++i)
        pf[i] = dp[i][R] + dp[i][W];
    for (int i = 1; i < N; ++i)
        pf[i] += pf[i-1];
    for (int i = 0; i < t; ++i) {
        int a, b; re(a, b);
        ps(pf[b] - pf[a-1]);
    }
}

